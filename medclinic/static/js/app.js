(function() {
    document.addEventListener('DOMContentLoaded', () => {
        if (window.initHeroSlider) window.initHeroSlider();
        if (window.initPopularSlider) window.initPopularSlider();
        if (window.initAccordions) window.initAccordions();
        if (window.initChatPopup) window.initChatPopup();
        if (window.initMobileMenu) window.initMobileMenu();
        if (window.initCartCounter) window.initCartCounter();
        if (window.initDesktopNav) window.initDesktopNav();
        if (window.initSearchModal) window.initSearchModal();
    });
})();